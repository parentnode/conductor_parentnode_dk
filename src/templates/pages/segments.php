	<h1>Conductor segmentation</h1>

	<p>
		Conductor is derived from a extensive, continuous and unparalleled analysis of browsers, conducted for more than 10 years. 
		It groups browsers and devices into 9 segments, based on common denominators for screen resolution, input
		method and scripting/styling capabilities.
	</p>

	<h2>The segments</h2>
	<dl class="segments">
		<dt>basic</dt>
		<dd>Semantic content. For search engines, screen readers for visually impaired, text-based browsers.</dd>

		<dt>desktop</dt>
		<dd>
			The most modern browsers. Chrome 5+, Firefox 4+ and Safari 5+. Support for modern technology, 
			like CSS3 transitions.
		</dd>

		<dt>desktop_ie</dt>
		<dd>
			Internet Explorer 9, 10 and 11. Lacking support for basic W3C recommendation, thus requiring workarounds and hack, 
			which will slow down standard compliant browsers.
		</dd>

		<dt>desktop_light</dt>
		<dd>
			All older browsers for desktop or laptop computers. Internet Explorer &lt; 8, 7, 6, Firefox &lt; 4, 
			Chrome &lt; 5, Safari &lt; 5, Netscape. You get the idea.
		</dd>

		<dt>mobile_touch</dt>
		<dd>
			Smartphones with touch capabilities broad W3C recommendation support. A screen width of minimum 320 pixels. 
			All iOS version and Android &gt; 2.2.
		</dd>

		<dt>mobile</dt>
		<dd>Newer mobile and feature phones and smartphones with screen width of less than 320 pixels.</dd>

		<dt>mobile_light</dt>
		<dd>All older mobile phones, with HTML capable browser.</dd>

		<dt>tablet</dt>
		<dd>Tablet computers. iPads and Android 2.2+ tablets with screen size of more than 5 inches.</dd>

		<dt>tv</dt>
		<dd>TV and console devices with internet access, using the remote control or gamepad for site navigation.</dd>
	</dl>

</div>