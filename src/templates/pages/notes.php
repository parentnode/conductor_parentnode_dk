<h3>Note junk</h3>
<p>
	There is no way of knowing all devices, and for some time this lack of information
	has given rise to a world of workaounds and hacks, in an attempt to support to most commonly used devices.
	Developers, as well as designers, has been forced to degrade user experience in one way or another, commenly
	seen as load overhead and exponentially growing frontend complexity.


	The days, of using one layout and
	either lowering your ambitions to meet the lowest common denominator, or jumping the fence in the low end
	leaving the low-end users out, are over.
</p>
